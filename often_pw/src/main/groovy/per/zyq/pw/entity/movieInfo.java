public class movieInfo {
    private String name;
    private String size;
    private String dizhi;
    private String s;

    public String getName() {
        return name;
    }

    public movieInfo setName(String name) {
        this.name = name;
        return this;
    }

    public String getSize() {
        return size;
    }

    public movieInfo setSize(String size) {
        this.size = size;
        return this;
    }

    public String getDizhi() {
        return dizhi;
    }

    public movieInfo setDizhi(String dizhi) {
        this.dizhi = dizhi;
        return this;
    }

    public String getS() {
        return s;
    }

    public movieInfo setS(String s) {
        this.s = s;
        return this;
    }
}
